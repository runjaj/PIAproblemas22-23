### 8. Conservación por calor

#### Problema 11.

En una empresa productora de alimento para animales de compañía tienen que diseñar el proceso de esterilización del producto. Se trata de un alimento con consistencia de paté. Se dispone de datos de termorresistencia del microorganismo objetivo:

|t (min)\\T (°C) | 114 | 117 | 120 |
|----------------|-----|-----|--------|
|0 | 1.0E+06 | 1.0E+06 | 1.0E+06|
|4 | 5.8E+05 | 1.8E+05 | 5.2E+03|
|6 | 4.4E+05 | 7.9E+04 | 3.7E+02|
|8 | 3.4E+05 | 3.4E+04 | 2.7E+01|
|10| 2.5E+05 | 1.5E+04 | < 30|

Se plantea lograr con el tratamiento térmico 8 reducciones decimales del microorganismo en cuestión tratándolo a 116 °C. Para esterilizar las conservas se utiliza un autoclave con las siguientes curvas t-T de calentamiento y enfriamiento:

_Calentamiento_

|t (min) | T (°C)|
|--------|-------|
|0 | 30|
|6 | 40.9|
|12 | 85.4|
|18 | 111.5|
|24  | 116|

_Enfriamiento_

|t (min) | T (°C)|
|--------|-------|
|0 | 116|
|2.5 | 72.1|
|5 | 52.1|
|7.5 | 47.2|
|10 | 45|

Contestar a las siguientes preguntas:
{type=a}
1. Demostrar que para el microorganismo a tratar _z_ = 6 °C y ``D_{121}`` = 1.2 min
2. Calcular el tiempo (real) de la etapa de mantenimiento
3. ¿Dónde se encuentra el punto crítico de esta conserva?
4. ¿Se puede tratar este producto en una instalación en continuo? Si es así, describir dicho equipo
"""

# ╔═╡ 9e28fc4a-c789-48d9-bad0-49ef9a28ee44
md"""
##### Solución

###### a.

Este apartado se puede realizar de dos manera. La primera es comprobar que con los valores de z = 6 °C y $D_{121}$ = 0.5 min se obtienen valores razonablemente similares a los resultados expermientales que nos proporciona el problema.

Combiando las dos leyes de la esterilización térmica se obtiene la dependencia de _n_ con el tiempo y la temperatura:

$$F_T = F_{Tref} 10^\frac{Tref-T}{z}=D_{Tref} n 10^\frac{Tref-T}{z}$$
$$n = \frac{F_T}{D_{Tref}} 10^\frac{T-Tref}{z}$$

A continuación calculamos _n_, el valor estimado de _n_ y la diferencia de ambos:
"""

# ╔═╡ f76236e3-a16d-4f03-ac7b-6963239929e5
D₁₂₁ = 1.2

# ╔═╡ 3cfa43bc-65c7-4768-a89d-70dc08c98f5f
z = 6

# ╔═╡ 26171260-e305-420c-90b5-6aae53ea8fec
begin
	# Datos del problema
	termo = DataFrame(t = repeat([0.,4.,6.,8.,10.], 3), T=[repeat([114.0], 5);repeat([117.0], 5);repeat([120.0], 5)], N=[1.0e6, 5.8e5, 4.4e5, 3.4e5, 2.5e5, 1e6, 1.8e5, 7.9e4, 3.4e4, 1.5e4, 1e6, 5.2e3, 3.7e2, 2.7e1, 1])
	# Cálculo de n experimental
	insertcols!(termo, :n => @. log10(1e6/termo.N))
	# Cálculo del n estimado con z = 6 °C y D121 = 1.2 min
	insertcols!(termo, :nₑₛₜ => @. termo.t/D₁₂₁*10^((termo.T-121)/z))
	# Diferencia entre n y n estimado
	insertcols!(termo, :diff => termo.n-termo.nₑₛₜ)
end

# ╔═╡ f1301543-9cb0-444b-a6e1-d17895cb960b
md"""
Comprobamos que las diferencias son razonablemente pequeñas.

La otra manera de dar respuesta a este apartado es determinar los valores de $z$ y de $D_{121}$ mediante regresión no lineal.

Para hacer el ajuste, definimos el modelo matemático usando la ecuación vista más arriba y realizamos el ajuste:
"""

# ╔═╡ d5fa55b9-dde2-4cd1-be39-f03a9503d217
# x[:,1] es el tiempo (FT)
# x[:,2] es la temperatura (T)
# p[1] es D121
# p[2] es z

@. model(x, p) = x[:,1]/(p[1])*10^((x[:,2]-121)/p[2])

# ╔═╡ a74a492f-0aec-4629-85c1-6b9700854640
fit = curve_fit(model, hcat(termo.t, termo.T), termo.n, [1.0, 1.0]; autodiff=:forwarddiff)

# ╔═╡ b4808733-5e6d-485b-9bec-0898fc489393
md"""
Los valores de $z$ y de $D_{121}$ obtenidos son:
"""

# ╔═╡ e7795587-a88a-4c6f-9e10-61d5ebe32c19
D121_fit = coef(fit)[1]

# ╔═╡ a84a38d8-bdde-422b-ac3e-83b723f6bae8
z_fit = coef(fit)[2]

# ╔═╡ 7c75ca4d-2da7-4d44-a44a-874919d32a25
md"""
Comprobamos que se ajustan a los valores proporcionados en el enunciado del problema.

Los márgenes de error del ajuste son:
"""

# ╔═╡ 96381c4e-52d5-4abf-92a1-6d91c9cc4326
margin_error(fit)

# ╔═╡ 1763941b-bd67-4af7-b176-bd37248411b6
md"""
Podemos verificar el ajuste mediante representación gráfica:
"""

# ╔═╡ cb11595e-0507-40cb-99eb-397f8784b5d4
begin
	n(t, T) = t/D121_fit*10^((T-121)/z_fit)
	tplot = (0:.1:10)
	Tplot = (114:.1:120)
	surface(tplot, Tplot, n)
	scatter!(termo.t, termo.T, termo.n, legend=false, xlabel="t", ylabel="T", zlabel="n")
end

