# ╔═╡ d465d01a-c52b-4b52-9f61-b889f172d1c5
md"""
El objetivo es alcanzar 8 reducciones decimales a 116 °C. Tomaremos la temperatura de la etapa de mantenimiento como temperatura de referencia:
"""

# ╔═╡ 9d825db8-c2e5-4eba-a61c-e0910ffa5a34
n_obj = 8

# ╔═╡ d7d201f9-6de4-4cc2-992b-1a7cf61bf88e
Tref = 116

# ╔═╡ 8c2f1a69-29c7-4557-add4-31e188e24a73
md"""
El valor esterlizante será:
"""

# ╔═╡ 8a68d5f3-c9c1-47e1-875f-e5d76683ed57
DTref = D₁₂₁*10^((121-Tref)/z)

# ╔═╡ 0ef77f56-9846-438b-b5a0-293cad90153a
FTref = DTref*n_obj

# ╔═╡ c8f0de75-058e-472e-bcf3-99ad5630e7f5
md"""
Ya solo queda calcular las letalidades de las etapas de calentamiento y de enfriamiento. Realizaremos las integrales por el método de Simpson ya que los puntos son equiespaciados en el tiempo y tenemos un número impar de punto:
"""

# ╔═╡ 7c55a783-1465-4b1b-9524-43c813293cbd
simpson(f) = (f.t[2]-f.t[1])/3*(f.f[1]+f.f[end] + 4*sum(f.f[2:2:end-1]) + 2*sum(f.f[3:2:end-2]))

# ╔═╡ 9e660904-22fc-47de-a864-254b4a0c7410
md"""
Calculamos la letalidad de la etapa de calentamiento:
"""

# ╔═╡ ba849157-ccf9-4681-9be7-94899492749a
insertcols!(cal, :f => @. 10^((cal.T-Tref)/z))

# ╔═╡ 8206d850-3d78-4b85-a592-355d19b848f5
L₁ = simpson(cal)

# ╔═╡ f48d2469-648f-41a2-92ff-ca5be2c0ec54
md"""
La letalidad de la etapa de enfriamiento es:
"""

# ╔═╡ 49788695-7468-4df1-87ff-8b73728cbd9a
insertcols!(enf, :f => @. 10^((enf.T-Tref)/z))

# ╔═╡ 2d24efee-b96b-4bdc-be49-08eef6c5ac39
L₃ = simpson(enf)

# ╔═╡ c16f64fe-23f3-4dad-ae84-2c6fc3d83946
md"""
La duración de la etapa de mantenimiento es:
"""

# ╔═╡ f47dac41-e24f-4482-9967-ba88dc6c4535
L₂ = round(FTref - L₁ - L₃; digits=2)

